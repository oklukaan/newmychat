The first thing to do is to create a new project in Visual Studio.

Once the project is created, open the Solution Explorer and add a new folder called “Scripts”. This folder will contain all of the JavaScript files that will be used in the project.

Next, add a new HTML page to the project and name it “index.html”. This page will be used as the main page for the application.

In the index.html file, add a reference to the jQuery library by adding a script tag with a src attribute pointing to the jQuery library file: 
```<script src="https://ajax.googleapis.com/ajax/libs/jquery/3.3.1/jquery.min.js"></script>``` 
This will allow us to use jQuery in our application code. 

Next, add another script tag with a src attribute pointing to our own JavaScript file:  ```<script src="scripts/main.js"></script>```  This will allow us to write our own custom JavaScript code for our application in this file. 

Now we can start writing our own custom JavaScript code in main.js file inside of the Scripts folder we created earlier. We can use jQuery functions and methods inside of this file to manipulate elements on our page or make AJAX requests to external APIs or web services for data retrieval and manipulation purposes. 

Once we have written all of our custom JavaScript code, we can save it and then open up index.html in a web browser to see how it works!

